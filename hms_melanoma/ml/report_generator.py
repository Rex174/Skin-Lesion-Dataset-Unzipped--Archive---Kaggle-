import json
from datetime import datetime
from pathlib import Path

from reportlab.lib.pagesizes import A4
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import cm
from reportlab.lib import colors
from reportlab.platypus import (SimpleDocTemplate, Paragraph, Spacer, Table,
                                 TableStyle, HRFlowable)
from reportlab.lib.enums import TA_LEFT, TA_CENTER, TA_RIGHT


# ── Colour palette (matches HMS UI) ─────────────────────────────────────────────
TEAL_DARK  = colors.HexColor("#0D5C4A")
TEAL_MID   = colors.HexColor("#1A7A62")
TEAL_LIGHT = colors.HexColor("#E8F4F1")
SLATE      = colors.HexColor("#4A5568")
RED_RISK   = colors.HexColor("#C0392B")
AMBER_RISK = colors.HexColor("#E67E22")
GREEN_RISK = colors.HexColor("#27AE60")
LIGHT_GRAY = colors.HexColor("#F7F7F7")
WHITE      = colors.white
BLACK      = colors.black


def _risk_color(risk_level: str):
    return {"high": RED_RISK, "moderate": AMBER_RISK, "low": GREEN_RISK}.get(risk_level, BLACK)


def generate_patient_pdf(
    patient,
    doctor,
    history: list,
    checks: list,
    output_path: str,
    class_labels: dict,
):
    """
    Generate a comprehensive PDF patient report.

    Parameters:
        patient      : Patient ORM object
        doctor       : DoctorProfile ORM object
        history      : list of MedicalHistory ORM objects
        checks       : list of MelanomaCheck ORM objects
        output_path  : full file path to save the PDF
        class_labels : dict mapping dx code -> full label name
    """
    doc = SimpleDocTemplate(
        output_path,
        pagesize=A4,
        leftMargin=2*cm, rightMargin=2*cm,
        topMargin=2.5*cm, bottomMargin=2*cm,
    )

    styles = getSampleStyleSheet()

    # ── Custom styles ─────────────────────────────────────────────────────────
    title_style = ParagraphStyle(
        "ReportTitle",
        parent=styles["Title"],
        fontSize=20, textColor=TEAL_DARK,
        spaceAfter=6, alignment=TA_CENTER,
    )
    subtitle_style = ParagraphStyle(
        "Subtitle",
        parent=styles["Normal"],
        fontSize=10, textColor=SLATE,
        spaceAfter=12, alignment=TA_CENTER,
    )
    section_header_style = ParagraphStyle(
        "SectionHeader",
        parent=styles["Heading2"],
        fontSize=13, textColor=WHITE,
        backColor=TEAL_DARK,
        spaceAfter=8, spaceBefore=14,
        leftIndent=-10, rightIndent=-10,
        borderPadding=(4, 8, 4, 8),
    )
    field_label_style = ParagraphStyle(
        "FieldLabel",
        parent=styles["Normal"],
        fontSize=9, textColor=SLATE, fontName="Helvetica-Bold",
    )
    field_value_style = ParagraphStyle(
        "FieldValue",
        parent=styles["Normal"],
        fontSize=10, textColor=BLACK,
    )
    disclaimer_style = ParagraphStyle(
        "Disclaimer",
        parent=styles["Normal"],
        fontSize=8, textColor=SLATE,
        borderColor=TEAL_MID, borderWidth=1,
        borderPadding=8, backColor=TEAL_LIGHT,
        spaceAfter=8,
    )
    note_style = ParagraphStyle(
        "Note",
        parent=styles["Normal"],
        fontSize=9, textColor=SLATE, italic=True,
    )

    story = []

    # ── HEADER ────────────────────────────────────────────────────────────────
    story.append(Paragraph("HMS — Melanoma Detection Report", title_style))
    story.append(Paragraph(
        f"Generated on: {datetime.utcnow().strftime('%d %B %Y, %H:%M UTC')} &nbsp;|&nbsp; "
        f"Report prepared by: Dr. {doctor.full_name}",
        subtitle_style
    ))
    story.append(HRFlowable(width="100%", thickness=2, color=TEAL_DARK, spaceAfter=12))

    # ── DISCLAIMER ────────────────────────────────────────────────────────────
    story.append(Paragraph(
        "<b>DISCLAIMER:</b> This report is generated by an AI-assisted melanoma screening "
        "system for clinical decision support only. It does not constitute a definitive "
        "medical diagnosis. All findings must be reviewed and confirmed by a qualified "
        "dermatologist. The AI model used in this system was trained to be equitable across "
        "demographic subgroups (age, sex, anatomical site) as part of a bias-reduction framework.",
        disclaimer_style
    ))

    # ── SECTION 1: PATIENT DEMOGRAPHICS ──────────────────────────────────────
    story.append(Paragraph("1. Patient Information", section_header_style))

    demo_data = [
        ["Full Name",       patient.full_name,
         "Patient ID",      str(patient.id)],
        ["Date of Birth",   str(patient.date_of_birth),
         "Age",             f"{patient.age} years ({patient.age_group})"],
        ["Sex",             patient.sex.capitalize(),
         "Contact",         patient.contact_number or "—"],
        ["Assigned Doctor", f"Dr. {doctor.full_name}",
         "Specialization",  doctor.specialization],
    ]
    demo_table = Table(demo_data, colWidths=[3.5*cm, 5.5*cm, 3.5*cm, 5*cm])
    demo_table.setStyle(TableStyle([
        ("FONTNAME",    (0,0), (-1,-1), "Helvetica"),
        ("FONTNAME",    (0,0), (0,-1), "Helvetica-Bold"),
        ("FONTNAME",    (2,0), (2,-1), "Helvetica-Bold"),
        ("FONTSIZE",    (0,0), (-1,-1), 10),
        ("TEXTCOLOR",   (0,0), (0,-1), SLATE),
        ("TEXTCOLOR",   (2,0), (2,-1), SLATE),
        ("BACKGROUND",  (0,0), (-1,-1), LIGHT_GRAY),
        ("ROWBACKGROUNDS", (0,0), (-1,-1), [WHITE, LIGHT_GRAY]),
        ("GRID",        (0,0), (-1,-1), 0.5, colors.HexColor("#CCCCCC")),
        ("TOPPADDING",  (0,0), (-1,-1), 5),
        ("BOTTOMPADDING",(0,0), (-1,-1), 5),
        ("LEFTPADDING", (0,0), (-1,-1), 6),
    ]))
    story.append(demo_table)
    story.append(Spacer(1, 0.4*cm))

    # ── SECTION 2: MELANOMA CHECK RESULTS ────────────────────────────────────
    story.append(Paragraph("2. AI Melanoma Screening Results", section_header_style))

    if not checks:
        story.append(Paragraph("No melanoma checks recorded for this patient.", note_style))
    else:
        for i, check in enumerate(checks, 1):
            ts_str   = check.timestamp.strftime("%d %B %Y, %H:%M")
            cls_full = class_labels.get(check.predicted_class, check.predicted_class)
            risk_col = _risk_color(check.risk_level)
            conf_pct = f"{check.confidence_score * 100:.1f}%"
            by_str   = "Doctor" if check.performed_by == "doctor" else "Self-Check"
            model_str= "Enhanced v2 (Debiased)" if check.model_used == "enhanced_v2" else "Baseline"

            check_data = [
                [f"Check #{i}  —  {ts_str}", "", "", ""],
                ["Predicted Class", cls_full, "Confidence", conf_pct],
                ["Risk Level",      check.risk_level.upper(), "Model Used", model_str],
                ["Performed By",    by_str, "Check ID",   str(check.id)],
            ]
            check_table = Table(check_data, colWidths=[3.5*cm, 6*cm, 3.5*cm, 4.5*cm])
            check_table.setStyle(TableStyle([
                # Header row
                ("SPAN",        (0,0), (3,0)),
                ("BACKGROUND",  (0,0), (3,0), TEAL_LIGHT),
                ("TEXTCOLOR",   (0,0), (3,0), TEAL_DARK),
                ("FONTNAME",    (0,0), (3,0), "Helvetica-Bold"),
                ("FONTSIZE",    (0,0), (3,0), 10),
                # Body
                ("FONTNAME",    (0,1), (0,-1), "Helvetica-Bold"),
                ("FONTNAME",    (2,1), (2,-1), "Helvetica-Bold"),
                ("TEXTCOLOR",   (0,1), (0,-1), SLATE),
                ("TEXTCOLOR",   (2,1), (2,-1), SLATE),
                ("FONTSIZE",    (0,1), (-1,-1), 9),
                ("ROWBACKGROUNDS", (0,1), (-1,-1), [WHITE, LIGHT_GRAY]),
                ("GRID",        (0,0), (-1,-1), 0.5, colors.HexColor("#CCCCCC")),
                ("TOPPADDING",  (0,0), (-1,-1), 4),
                ("BOTTOMPADDING",(0,0),(-1,-1), 4),
                ("LEFTPADDING", (0,0), (-1,-1), 6),
                # Risk level colour
                ("TEXTCOLOR",   (1,2), (1,2), risk_col),
                ("FONTNAME",    (1,2), (1,2), "Helvetica-Bold"),
            ]))
            story.append(check_table)

            # Fairness note
            if check.fairness_note:
                story.append(Spacer(1, 0.2*cm))
                story.append(Paragraph(
                    f"<b>Fairness Note:</b> {check.fairness_note}",
                    disclaimer_style
                ))

            # Doctor notes
            if check.notes:
                story.append(Spacer(1, 0.2*cm))
                story.append(Paragraph(
                    f"<b>Clinical Notes:</b> {check.notes}", field_value_style
                ))

            story.append(Spacer(1, 0.4*cm))

    # ── SECTION 3: MEDICAL HISTORY ────────────────────────────────────────────
    story.append(Paragraph("3. Medical History", section_header_style))

    if not history:
        story.append(Paragraph("No medical history records found.", note_style))
    else:
        hist_header = [["Date", "Diagnosis", "Clinical Notes", "Follow-Up"]]
        hist_rows   = []
        for rec in history:
            fu_str = "Yes"
            if rec.follow_up and rec.follow_up_date:
                fu_str = f"Yes ({rec.follow_up_date})"
            elif not rec.follow_up:
                fu_str = "No"
            hist_rows.append([
                str(rec.visit_date),
                rec.diagnosis or "—",
                (rec.clinical_notes or "—")[:200],
                fu_str,
            ])

        hist_table = Table(
            hist_header + hist_rows,
            colWidths=[2.5*cm, 4*cm, 8.5*cm, 2.5*cm]
        )
        hist_table.setStyle(TableStyle([
            ("BACKGROUND",  (0,0), (-1,0), TEAL_DARK),
            ("TEXTCOLOR",   (0,0), (-1,0), WHITE),
            ("FONTNAME",    (0,0), (-1,0), "Helvetica-Bold"),
            ("FONTSIZE",    (0,0), (-1,-1), 9),
            ("ROWBACKGROUNDS",(0,1),(-1,-1), [WHITE, LIGHT_GRAY]),
            ("GRID",        (0,0), (-1,-1), 0.5, colors.HexColor("#CCCCCC")),
            ("TOPPADDING",  (0,0), (-1,-1), 4),
            ("BOTTOMPADDING",(0,0),(-1,-1), 4),
            ("LEFTPADDING", (0,0), (-1,-1), 6),
            ("VALIGN",      (0,0), (-1,-1), "TOP"),
        ]))
        story.append(hist_table)

    story.append(Spacer(1, 0.8*cm))

    # ── FOOTER ────────────────────────────────────────────────────────────────
    story.append(HRFlowable(width="100%", thickness=1, color=TEAL_MID, spaceAfter=8))
    story.append(Paragraph(
        f"This report was automatically generated by the FYP HMS — Melanoma Detection System "
        f"(TP070818 | APD3F2511CS DA). &nbsp; Report ID: {patient.id}_{datetime.utcnow().strftime('%Y%m%d%H%M%S')}",
        ParagraphStyle("Footer", parent=styles["Normal"],
                       fontSize=7, textColor=SLATE, alignment=TA_CENTER)
    ))

    doc.build(story)
    print(f"[OK] PDF report saved -> {output_path}")
